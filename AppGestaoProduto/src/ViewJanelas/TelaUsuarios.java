    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {   
            // 1. Pega os textos que você digitou nas caixinhas
            String matricula = jTextField1.getText().trim();
            String nome = jTextField2.getText().trim();
            String endereco = jTextField3.getText().trim();
            String tipo = (String) jComboBox1.getSelectedItem();
            String cursoOuDepto = jTextField4.getText().trim();

            // 2. Trava de segurança para não salvar vazio
            if (matricula.isEmpty() || nome.isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Por favor, preencha a Matrícula e o Nome!", "Aviso", javax.swing.JOptionPane.WARNING_MESSAGE);
                return;
            }

            // 3. A Mágica da Herança: Decide se cria um Aluno ou Professor
            modelo.Usuario novoUsuario;
            
            if (tipo.equals("Aluno")) {
                novoUsuario = new modelo.Aluno(nome, matricula, endereco, cursoOuDepto);
            } else {
                novoUsuario = new modelo.Professor(nome, matricula, endereco, cursoOuDepto);
            }

            // 4. Chama o DAO para fazer o INSERT INTO no banco
            DAO.UsuarioDAO dao = new DAO.UsuarioDAO();
            dao.cadastrar(novoUsuario);

            // 5. Exibe a mensagem de sucesso e limpa as caixinhas
            javax.swing.JOptionPane.showMessageDialog(this, "Usuário cadastrado com sucesso!", "Sucesso", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jComboBox1.setSelectedIndex(0);

        atualizarTabela(); // COLOQUE ESTA LINHA AQUI!

        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao salvar usuário: " + e.getMessage(), "Erro", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    
    }//GEN-LAST:event_jButton1ActionPerformed

