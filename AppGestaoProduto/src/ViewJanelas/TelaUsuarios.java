    private void atualizarTabela() {
        try {
            DAO.UsuarioDAO dao = new DAO.UsuarioDAO();
            java.util.List<modelo.Usuario> lista = dao.listarTodos();

            javax.swing.table.DefaultTableModel modeloTabela = (javax.swing.table.DefaultTableModel) jTable1.getModel();
            
            modeloTabela.setColumnIdentifiers(new Object[]{"Matrícula", "Nome", "Endereço", "Tipo", "Curso/Depto"});
            modeloTabela.setNumRows(0);

            for (modelo.Usuario u : lista) {
                String tipo = "";
                String cursoDepto = "";
                
                if (u instanceof modelo.Aluno) {
                    tipo = "Aluno";
                    cursoDepto = ((modelo.Aluno) u).getCurso();
                } else if (u instanceof modelo.Professor) {
                    tipo = "Professor";
                    cursoDepto = ((modelo.Professor) u).getDepartamento();
                }

                modeloTabela.addRow(new Object[]{
                    u.getMatricula(),
                    u.getNome(),
                    u.getEndereco(),
                    tipo,
                    cursoDepto
                });
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao carregar a tabela: " + e.getMessage());
        }
    }

