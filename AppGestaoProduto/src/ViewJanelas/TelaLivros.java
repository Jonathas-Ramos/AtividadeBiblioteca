    private void adicionarBntActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adicionarBntActionPerformed
// 1. Capturando os dados que você digitou na tela
        String idLivro = jTextField1.getText().trim();
        String tituloLivro = jTextField2.getText().trim();
        String autorLivro = jTextField3.getText().trim(); // Pega o Autor
        String categoria = (String) jComboBox1.getSelectedItem();
        
        // 2. Trava para garantir que os textos não estão vazios
        if (idLivro.isEmpty() || tituloLivro.isEmpty() || autorLivro.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Por favor, preencha ID, Título e Autor!", "Aviso", javax.swing.JOptionPane.WARNING_MESSAGE);
            return; 
        }

        // 3. Convertendo a quantidade (que é texto) para número (int)
        int quantidade = 0;
        try {
            quantidade = Integer.parseInt(jTextField4.getText().trim()); // Pega a Quantidade
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "A quantidade deve ser apenas números!", "Aviso", javax.swing.JOptionPane.WARNING_MESSAGE);
            return;
        }

        // 4. Mandando salvar e mostrando o sucesso!
        try {
            // Passa todos os dados reais (usando 0 para o ano, como combinamos)
            controleBiblioteca.cadastrarNovoLivro(idLivro, tituloLivro, autorLivro, 0, quantidade, categoria);
            
            javax.swing.JOptionPane.showMessageDialog(this, "Livro adicionado com sucesso!", "Sucesso", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            
            // Limpa todos os campos da tela
            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jComboBox1.setSelectedIndex(0);
            
            atualizarTabela();
            
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao salvar: " + e.getMessage(), "Erro", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_adicionarBntActionPerformed

