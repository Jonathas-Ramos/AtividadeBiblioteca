    private void atualizarTabela() {
        try {
            // 1. Puxa a lista de livros usando o seu listarTodos()
            DAO.LivroDAO dao = new DAO.LivroDAO();
            java.util.List<modelo.Livro> lista = dao.listarTodos();

            // 2. Pega o "molde" da tabela visual que você arrastou para a tela
            javax.swing.table.DefaultTableModel modeloTabela = (javax.swing.table.DefaultTableModel) jTable2.getModel();
            
            // 3. Arruma os nomes das colunas da tabela
            modeloTabela.setColumnIdentifiers(new Object[]{"ID", "Título", "Autor", "Categoria", "Qtd"});
            
            // 4. Limpa a tabela antes de preencher, para não duplicar visualmente
            modeloTabela.setNumRows(0);

            // 5. Para cada livro do banco de dados, cria uma linha na tabela
            for (modelo.Livro livro : lista) {
                modeloTabela.addRow(new Object[]{
                    livro.getId(),
                    livro.getTitulo(),
                    livro.getAutor(),
                    livro.getCategoria(),
                    livro.getQuantidadeDisponivel()
                });
            }
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao carregar a tabela: " + e.getMessage());
        }
    }

